MIN_SCORE = sum(min(p for _, p in q["options"]) for q in QUESTIONS)   # ۱۳
MAX_SCORE = sum(max(p for _, p in q["options"]) for q in QUESTIONS)   # ۴۷


def questions_payload() -> list[dict[str, Any]]:
    """ساختار پرسش‌نامه برای فرانت‌اند (بدون افشای امتیازها)."""
    return [
        {"id": i, "q": q["q"], "options": [opt[0] for opt in q["options"]]}
        for i, q in enumerate(QUESTIONS)
    ]


def _category(pct: float) -> dict[str, str]:
    """طبقهٔ ریسک فارسی + توضیح + رنگ برند، بر پایهٔ درصد ۰–۱۰۰."""
    if pct < 20:
        return {"key": "very_conservative", "label": "بسیار محافظه‌کار",
                "desc": "حفظ اصل سرمایه برایتان از همه‌چیز مهم‌تر است و از نوسان شدید پرهیز می‌کنید.",
                "color": "#16C784"}
    if pct < 40:
        return {"key": "conservative", "label": "محافظه‌کار",
                "desc": "ثبات را به رشد سریع ترجیح می‌دهید و ریسک محدود را می‌پذیرید.",
                "color": "#4ED9CC"}
    if pct < 60:
        return {"key": "moderate", "label": "متعادل",
                "desc": "تعادلی میان رشد و امنیت می‌خواهید و نوسان معقول را تحمل می‌کنید.",
                "color": "#2D63B0"}
    if pct < 80:
        return {"key": "aggressive", "label": "ریسک‌پذیر",
                "desc": "برای بازدهی بالاتر، نوسان قابل‌توجه را می‌پذیرید.",
                "color": "#F59E0B"}
    return {"key": "very_aggressive", "label": "بسیار ریسک‌پذیر",
            "desc": "به دنبال بیشترین رشد هستید و با ریسک و نوسان بالا کاملاً راحتید.",
            "color": "#EA3943"}


def score_answers(answers: list[int]) -> dict[str, Any]:
    """answers: فهرست اندیس گزینهٔ انتخابی برای هر پرسش (به ترتیب).
    خروجی: امتیاز خام، درصد، طبقهٔ ریسک."""
    if len(answers) != len(QUESTIONS):
        raise ValueError(f"تعداد پاسخ‌ها باید {len(QUESTIONS)} باشد")
    raw = 0
    for qi, choice in enumerate(answers):
        opts = QUESTIONS[qi]["options"]
        if not isinstance(choice, int) or choice < 0 or choice >= len(opts):
            raise ValueError(f"گزینهٔ نامعتبر برای پرسش {qi}")
        raw += opts[choice][1]
    pct = round((raw - MIN_SCORE) / (MAX_SCORE - MIN_SCORE) * 100, 1)
    cat = _category(pct)
    return {"raw": raw, "min": MIN_SCORE, "max": MAX_SCORE, "percent": pct, **cat}
